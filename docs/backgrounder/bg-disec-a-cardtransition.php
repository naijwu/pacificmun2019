              <h4>Rules of Procedure</h4>
              <p>
                Unlike general MUN committees, crisis committees have a different set of Rules of Procedures that set them apart and make them extremely fun to participate in. Therefore, the paragraphs below explain two concepts within crisis committees that makes participating in one so enthralling.
              </p>
              <h5>Perpetual Moderated Caucus</h5>
              <p>
                The concept of a Perpetual Moderated Caucus is foreign to most delegates; however, this is just one of the things that makes a crisis committee unique. This means that delegates will take turns to speak at one minute intervals, and they do not need to motion for a moderated or unmoderated caucus. Occasionally, the dias would ask for any motions, or present a crisis update. When a directive is approved by the dias, it will be presented and voted upon by the committee, and once it has been done, the debate shall resume.
              </p>
              <h5>Directives</h5>
              <p>
                Essentially, directives are like miniature resolution papers, or what action your committee will take towards a crisis update. When sending up a directive, delegates can choose between sending up a public directive, or a private directive. A public directive is a directive that requires a simple majority vote and contains anything that should be discussed and approved by the committee. Private directives only require the approval of one delegate, the delegate writing the directive, but the effect of this directive is greatly limited by the delegates personal power. The outcome  of the private directives will be revealed to individual via a note from the dias. On that note, we expect the directives to be reasonable and achievable by the powers you have.
              </p>
