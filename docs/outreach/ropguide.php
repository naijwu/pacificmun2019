        <h1>Rules of Procedure Guide</h1>
        <h2>Secretariat Team</h2>
        <h4>November 23, 2018</h4>
        <p>
          Understanding how Model United Nations committee sessions function is crucial for the best possible
          conference experience. While we strongly encourage any delegates to attend our beginner delegate training
          session on the first day of the conference, the guide below is a reference for delegates, new and old, in the
          rules and procedures (ROP) used during committee sessions. We implore all delegates to read over this
          guide in order to thoroughly understand ROP and get the best experience at the conference. That being
          said, this guide will be for all committees at PacificMUN 2018 except for the Joint Crisis Committee, the
          United Nations Space Command, the North Atlantic Treaty Organization, the United States Cabinet, and the
          Historical United Nations Security Council. Each of these committees have custom rules of procedure but
          generally adhere to this guide with some modifications.
        </p>
        <h5>Roll Call</h5>
        <p>
          At the beginning of each committee session, Roll Call is used to to establish quorum in the committee.
          When the gavel is knocked and the dais calls for Roll Call, the committee session has started and requires all
          delegates to remain silent as Roll Call is done. In alphabetical order, delegations are read-out from all those
          represented in the committee. As a delegate, you must listen carefully for your delegation to be called so
          you can respond in one of two ways. This is either by saying ‘present’ or ‘present and voting’. By declaring you
          are present, you establish your delegation’s presence in the committee and retain the right to either vote
          or abstain on resolution papers. However, by stating you are present and voting, you acknowledge your
          delegation’s presence in the committee and affirm that your delegation will be voting on resolution
          papers thus conceding your delegation’s right to abstain. At the end of Roll Call quorum will be set meaning
          that the committee has enough delegates to engage in debate, writing, voting, and other elements of the
          committee session.
        </p>
        <h5>Opening the Debate</h5>
        <p>
          To open the debate, the dais will call for any motions on the floor. The first motion must be to open debate.
          This is motioned by saying, “The delegate of (insert delegation here) motions to open debate.”
        </p>
        <h5>Primary Speakers List</h5>
        <p>
          The first thing needed in the committee is to decide which topic will be discussed first. This is done by
          setting the agenda. The Primary Speakers List is only used in two topic committees as it is a platform for
          delegates to express their delegation’s opinion on which topic should be debated first. Typically, the dais will
          call for any delegates willing to speak to raise their placards. If you wish to speak, raise your placard and you
          will be added to the list.
        </p>
        <h5>Setting the Agenda</h5>
        <p>
          The agenda only needs to be set in two topic committees. It establishes which topic will be discussed first.
          At the end of the Primary Speakers list, the dais will call for any points or motions. At this point a delegate
          may say, “The delegate of (insert delegation here) motions to set the agenda to Topic (either A or B.)” Once
          this motion is proposed the dais will call for two delegates to speak for this motion and two to speak against
          it. This is the only time a motion itself is discussed as it will set the course of events for the entire committee.
          The delegates will speak in the pattern of one for followed by one against and once all four delegates have
          spoken the motion will be voted upon, and the agenda be set.
        </p>
        <h5>Secondary Speakers List</h5>
        <p>
          With the agenda set, the committee moves on to the Secondary Speakers List. The Secondary Speakers List
          is a time for delegates to speak on the topic at hand in a general sense. The delegates need not focus on a
          particular aspect of the problem but instead address the broader ideas and concerns their nation has. When
          finished speaking, a delegate must yield their time. The different forms of yields are described in Appendix 2.
          At the dais’ discretion, they may call for any points or motions on the floor. A list of possible points are found
          below in Appendix 1. There are four motions a delegate may propose, they are as listed below:
        </p>
        <h6>Roundtables</h6>
        <p>
          A roundtable is a where the dais goes through each country via roll call where each will speak on a specific
          aspect of the topic. As each delegate usually speaks for thirty seconds, they are time consuming but also
          force every delegation to offer their perspective on the particular aspect of the topic in discussion. One
          proposes a roundtable by saying, “The delegate of (insert delegation here) motions for a roundtable on
          (insert particular aspect of the topic here) with a (insert speaking time per delegate here.)”
        </p>
        <h6>Unmoderated Caucus</h6>
        <p>
          An Unmoderated Caucus, more commonly known as an “unmod,” is a form of caucus that offers a unique
          chance for delegates to interact in unregulated debate and discussion. Unmoderated caucuses allow
          delegates to stand up and move around in order to discuss ideas with other delegates in an informal
          environment, continue progress in writing papers, and gain bloc support for their proposed solutions. In
          terms of length, unmods can vary from 5 minutes to 30 minutes. A delegate can propose an unmodby
          motioning, “The delegate of (insert delegation here) motions for a (insert time here) unmoderated caucus.”
        </p>
        <h6>Moderated Caucus</h6>
        <p>
          Making up the majority of each committee session, the moderated caucus is a form of structured and
          focus debate. It is purposed for the general topic to be broken down into specific aspects, which will then
          be discussed by the committee. For example, delegates can use the moderated caucus to focus debate on
          specific issues like a particular solution. Moderated Caucuses are unique as the delegate proposes both the
          speaking time per delegate followed by the total speaking time for all delegates in the Moderated Caucus.
          These are proposed through a motion which is phrased as such, “The delegate of (insert delegation here)
          motions for a Moderated Caucus on (insert particular aspect of the topic here) with a (insert total speaking
          time here : insert speaking time per delegate here.)”
          <br><br>
          Note: the speaking time per delegate is usually one minute and the total time is most commonly either five
          or ten minutes.
        </p>
        <h6>Working Paper Presentation</h6>
        <p>
          After extensive debate, delegates will be able to formulate a cluster of ideas which, once reviewed by the
          dais, becomes a working paper that can be presented to the committee. The committee will be able to read
          through the Working Paper and understand a bloc’s potential solutions.
          <br><br>
          <font style="font-style:italic">Question Period</font> <br>
          After the presentation of the Working Paper, the dais will ask for any points or motions, most commonly,
          this will be a motion for a question period of either 5, 10, or 15 minutes. In this time, delegates can ask
          questions to the sponsors of the Working Paper. (For more information on Working Papers, please refer to
          the Resolution Paper Guide on the PacificMUN website.)
        </p>
        <h5>Amendments</h5>
        <p>
          If a delegate wishes to alter a proposed Working Paper, they can use amendments to enact specific change.
          There are two types of amendments, both of which are described below. Typically, an amendment is
          formatted as if it was an operative clause in a resolution paper. Amendments to particular wordings are done
          by rewriting the operative clause as desired and then submitted towards the dais. (For more information on
          writing Amendments and formatting resolution papers, please refer to the Resolution Paper Guide on the
          PacificMUN website.)
        </p>
        <h6>Friendly Amendments</h6>
        <p>
          When proposed, a Friendly Amendment is accepted by all sponsors of a Working Paper and is immediately
          added to the Working Paper. These are usually minor changes in wording or slight alterations.
        </p>
        <h6>Unfriendly Amendments</h6>
        <p>
          When proposed, an Unfriendly Amendment is not accepted by the sponsors of a Working Paper and the
          decision on whether they are implemented into the Working Paper is put to a vote by the committee.
        </p>
        <h5>Voting Procedures</h5>
        <p>
          Once all proposed Working Papers have been read, questioned, and amended, the committee can move
          into votion procedures. This is done when the dais asks for any points or motions and a delegate motions
          to enter voting procedures by saying, “The delegate of (inset delegation here) motions to table debate and
          move into voting procedures.” When passed, the committee must ask all spectators to leave the room and
          the doors must be barred (locked).
        </p>
        <h5>Voting Selection</h5>
        <p>
          At this time, when delegates are asked for any points or motions, they can motion to re-order any Working
          Papers (now Draft Resolutions.) This may be to re-organize Draft Resolutions and change them from
          one numeric position to the other. For example, one may motion to change their resolution form Draft
          Resolution 1.1 to Draft Resolution 1.0.
        </p>
        <h5>Voting Methods</h5>
        <p>
          Delegates now must motion to vote on one of the Draft Resolutions and state what voting method the
          delegation would like to see used. For the full list of voting methods please see Appendix 3.
        </p>
        <h5>Voting</h5>
        <p>
          The dais will now use the approved voting method to determine whether or not a Draft Resolution passes
          and becomes a Resolution. Multiple resolutions may be passed, however passed resolutions cannot
          contradict each other, which may prevent some from passing.
          <br><br>
          * Steps 2-4 only occur during the first committee session. <br>
          * In a two-topic committee, the resolution of the first topic will cause the committee to begin with the second topic
          starting on Step 5.
        </p>
        <h5>Appendix I</h5>
        <h6>Points and Rights</h6>
        <p>
          The following are the points a delegate can propose and the rights they have. The points are listed in order
          of precedent in committee. If the dais does not call on your delegation when you raise your placard, please
          keep it raised as they may not be able to differentiate it with a motion.
          <br><br>
          <font style="font-style:italic; padding-bottom:10px;display:table;">Point of Order / Point of Parliamentary Procedure</font>
          A Point of Order or Point of Parliamentary Procedure is used when the delegate finds an issue with the
          committee’s execution of the rules of procedure. This should not focus on any content-related matters,
          instead an issue regarding the format of the flow of debate.
          <br><br>
          <font style="font-style:italic; padding-bottom:10px;display:table;">Point of Personal Privilege </font>
          A Point of Personal Privilege is used when delegates are physically discomforted in the conference by
          external factors that affect a delegate’s ability to participate in the committee session. This can include
          aspects such as the temperature of the room, the size of text displayed on the screen, or a simple lack of
          pens and paper.
          <br><br>
          Note: Delegates do not need to use a Point of Personal Privilege to leave the committee room to use
          the restroom or get a drink of water.
          <br><br>
          <font style="font-style:italic; padding-bottom:10px;display:table;">Point of Information / Point of Inquiry </font>
          The Point of Information / Point of Inquiry (POI) is used by delegates if they are confused or unaware of any
          aspect of the committee. This can include concerns with the topic, the rules of procedure, or any general
          information pertaining to the conference. Delegates can ask a POI whenever the dais asks for any points or
          motions.
          <br><br>
          <font style="font-style:italic; padding-bottom:10px;display:table;">Right of Reply </font>
          When a delegate feels that they have been personally offended by another delegate, either by their words
          or actions, a delegate can call for a Right of Reply. This can be done either by sending a note to the dais or
          raising one’s placard. The Right of Reply should only be used at the event a delegate is personally offended.
          This does not pertain to insults against a country, instead the insult must be directed at the delegate to
          warrant the Right of Reply.
        </p>
        <h5>Appendix II</h5>
        <h6>Yields</h6>
        <p>
          At the end of a speech on the Secondary Speakers List, a delegate must yield their remaining time, no matter
          what quantity is left. There are four types of yields that are recognized at PacificMUN.
          <br><br>
          <font style="font-style:italic; padding-bottom:10px;display:table;">Yield to the Dais</font>
          The most common yield, this yield gives the remainder of the time to the dais allowing the delegate to
          conclude their speech and sit down. The next delegate on the Secondary Speakers List will then be allowed
          to speak.
          <br><br>
          <font style="font-style:italic; padding-bottom:10px;display:table;">Yield to Another Delegate</font>
          Commonly used, this yield allows a delegate to give the remainder of their time to another delegate who
          will then be permitted to speak if they accept the yield. This delegate must further yield their time.
          <br><br>
          <font style="font-style:italic; padding-bottom:10px;display:table;">Yield to Questions</font>
          This form of yield allows a delegates to be questioned by the committee in the remainder of the time they
          have left.
          <br><br>
          <font style="font-style:italic; padding-bottom:10px;display:table;">Yield to Comments</font>
          This yield gives any delegate the right to comment on the current situation of the committee and address
          any concerns they may have.
        </p>
        <h5>Appendix III</h5>
        <h6>Types of Voting</h6>
        <p>
          <font style="font-style:italic; padding-bottom:10px;display:table;">Procedural Voting</font>
          When the committee votes on non-substantial matters, such as passing basic motions, the committee uses a
          simple majority of one more than half the delegates in the committee room. Any motion that aligns with the
          concept of voting on things relating to the procedure or flow the committee is a form of procedural voting
          and uses a simple majority.
          <br><br>
          <font style="font-style:italic; padding-bottom:10px;display:table;">Substantial Voting</font>
          When the committee votes on substantial matters, such as voting on a Draft Resolution, the committee uses
          a two-thirds majority so two-thirds of the delegates must vote to pass the substantial matter. Any motion
          that ultimately affects the committees decisions on handling certain topics are considered substantial.
          Apart from voting on clauses, divisions of clauses, draft resolutions, setting the agenda, and unfriendly
          amendments (which are all substantive matters,) all other votes are considered procedural.
        </p>
        <h6>Voting Methods</h6>
        <p>
          The methods of voting used in Model UN allow delegates to truly witness the success of their work in a way
          they deem fair and acceptable. Apart from voting by placard, the other methods of voting are only used
          when voting on substantial matters. Voting by placard is used for procedural voting and substantive voting.
          <br><br>
          <font style="font-style:italic; padding-bottom:10px;display:table;">Placard</font>
          The most commonly used voting procedure in MUN, voting by placard allows all those in favour of passing a
          motion to raise their placard and the dais to find the majority (either two-thirds or simple based on the type
          of voting.)
          <br><br>
          The following are only used for substantive voting:
          <br><br>
          <font style="font-style:italic; padding-bottom:10px;display:table;">Roll Call</font>
          In this method, the dais will go through all delegations on the roll call and each will be able to cast their vote
          either for or against the resolution. In addition to a simple “yes” or “no” delegates can also claim their stance
          “with rights.” Meaning that after the vote is finished they may make remarks to the committee on what their
          chosen stance is.
          <br><br>
          <font style="font-style:italic; padding-bottom:10px;display:table;">Acclamation</font>
          Through voting by acclamation, all delegates who oppose the resolution are asked to raise their placard. If
          any delegate opposes, the committee moves to another form of voting.
          <br><br>
          <font style="font-style:italic; padding-bottom:10px;display:table;">Division of the Question</font>
          Division of The Question allows delegates to group together clauses from a Draft Resolution and then
          evaluate them in groups. Each group of clauses will have two delegates speak proposing the clauses and
          two delegates speak opposing the clauses. During this time the clauses can be further amended. Once the
          four delegates have spoken and all amendments have been proposed and solved, the clauses will be voted
          on. These are considered to be substantive matters.
          <br><br>
          <font style="font-style:italic; padding-bottom:10px;display:table;">Clause-by-Clause</font>
          Voting Clause-by-Clause is the least common form of voting. Each clause must be amended individually,
          followed by two speakers proposing the clause and two other speakers opposing the clause. Then the clause
          will be voted upon as a substantive matter. While this method of voting is holistically comprehensive, it is
          also very time consuming.
        </p>
