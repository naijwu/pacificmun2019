        <p>
          Running a specialized committee, especially a crisis one, involves processing directives. Directives are individual operative clauses submitted by either groups of delegates, public directives, or by a single delegate, private directives, which act to impact the committee in real time. They may be clauses that mobilize military troops or taking certain actions which the dais needs to read over. Private directives are passed on the basis of dais decision if they seem to be realistic within the committee while public directives are voted on by the committee or straight approved by the daisâ€™ decision.
        </p>
        <h5>Perpetual Moderated Caucus</h5>
        <p>
          Unlike other committees with multiple speakers lists and caucuses, most crises committees only have perpetual moderated caucuses which resembles a time where delegates may raise there placard to speak about the crisis but do not need to yield there time. The unique factor of the perpetual moderated caucus is that delegates can propose unmoderated caucuses within this perpetual moderated caucus.
