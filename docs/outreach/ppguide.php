        <h1>Position Paper Guide</h1>
        <h2>Secretariat Team</h2>
        <h4>November 23, 2018</h4>
        <p>
          Before the conference, it is crucial that each delegate complete a position paper for each of the topics
          they will be discussing in their committee. This paper is the culmination of a delegate’s research, analysis,
          and synthesis. Position papers are an integral part to all Model United Nations conferences, as they help
          delegates solidify their understanding of both the topic and their country’s stance. Thus, position papers
          are mandatory for the delegates who wish to be eligible for an award, with the exception of delegates
          participating in ADHOC. Position papers must be emailed to your committee’s Dais by February 16th, 2019.
        </p>
        <h5>Significance</h5>
        <p>
          Position papers give staff members an indication of a delegate’s understanding and research of the topic.
This allows the staff members to anticipate and prepare for the flow of debate in the committee, as well as
use the paper as a reference when choosing awards. Typically, each position paper is approximately one
page of content and a bibliography. The position paper is a summary of the country’s stance on the issue,
and research for it gives the delegate a deeper understanding of their stance. Beyond the position paper,
delegates are encouraged to compile research into a research binder or an online document.
        </p>
        <h5>Research</h5>
        <p>
          In Model United Nations, research is the key to an engaging and effective debate. Position papers are
a culmination of that research and will showcase the delegate’s preparation for the sessions of debate,
discussion, and problem-solving. External research is vital for the delegate to understand the topics raised
in debate, and will enhance their Model UN experience. Reliable sources of information can include the
United Nations archives and websites, official government sources, political leaders’ speeches, and academic
compositions. A great way to begin researching is to use the discussion questions in the backgrounders.
        </p>
        <h5>Content Suggestions</h5>
        <p>
          While all topics and nations will have different policies, there are a few areas of discussion that may be
beneficial for delegates to write about.
        </p>
        <h6>Topic Background <font style="color:red; font-weight:bold;">(Red)</font> </h6>
        <p>
          What is the definition of the topic? <br>
          Where does the topic take place and who are the main stakeholders? <br>
          How are citizens effected? <br>
          When did this topic become an issue?
        </p>
        <h6>Past Action <font style="color:green; font-weight:bold;">(Green)</font> </h6>
        <p>
          What has your nation done to address the issue? <br>
          What are the treaties and resolutions your nation has helped implement? <br>
          Does this issue stem from your nation or is your nation indirectly involved? <br>
          What has the UN/your committee done to solve this issue? <br>
          Has the UN/your committee implemented any prior resolutions? <br>
          What are the different viewpoints on this issue?
        </p>
        <h6>Country Policy <font style="color:blue; font-weight:bold;">(Blue)</font> </h6>
        <p>
          Where does your country stand on the issue? <br>
          What have your political leaders said about this topic? <br>
          What are the statistics backing up your country's stance?
        </p>
        <h6>Possible Solutions <font style="color:#05AFEF; font-weight:bold;">(Sky)</font> </h6>
        <p>
          What is a possible solution that your country would support? <br>
          What are solutions that your country would not support? <br>
          What are existing frameworks that could be added upon? <br>
          How would this solution be funded? <br>
          How would this solution be implemented?
        </p>
        <h5>Formatting Requirements</h5>
        <p>
          <ul>
            <li>Delegate name, country, and topic</li>
            <li>Written in third-person</li>
            <li>Times New Romans, size 12</li>
            <li>Bibliography (MLA Citation)</li>
          </ul>
        </p>
