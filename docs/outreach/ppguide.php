            <strong>Topic A:</strong> The Empowerment of Women in Developing Countries <br>
            <strong>Committee:</strong> United Nations Human Rights Council <br>
            <strong>Country:</strong> United Kingdom of Great Britain and Northern Ireland <br>
            <strong>Delegate:</strong> Alice Li
            <br><br>
            <font style="color:red">
              Empowerment of women has been a fundamental topic for human rights. According to the United Nations
              Population Fund (UNFPA), “Women’s empowerment is vital to sustainable development and the realization of
              human rights for all.” According to Sara Longwe, women’s empowerment is defined as “the process by which
              women collectively come to recognize and address the gender issues that stand in the way of their advancement.
              In a patriarchal society, these gender issues are the practices of gender discrimination which are entrenched in
              custom, law and ideological belief”.
            <br><br>
              The United Nations Entity for Gender Equality and the Empowerment of Women, also known as the UN
              Women, states that there are seven primary principles of empowering women. Summarized, they are primarily
              to establish high-level leadership for gender equality, treat both genders fairly through work, education, safety
              and well-being, and promoting equality and empowerment through enterprise development and community
              initiatives.</font><font style="color:blue">
              As a sovereign member of the United Nations, the United Kingdom strongly favors the promotion of
              gender equality and empowerment of women. As the third Millennium Development Goal, promoting gender
              equality and empowering women and the fifth, improving maternal health. The United Kingdom notes with
              approval the strong necessity and importance of the empowerment of women. </font>
            <br><br>
              <font style="color:green">
              Historically, the United Kingdom has strived to empower women using multiple methods. Issues related to so
              are well established, with main concerns focusing on the pay, employment and services. In 1970, the Equal Pay
              Act passed, giving individuals within the UK the right of same pay and benefits as the opposite gender. The
              Sex Discrimination Act in 1975 banned gender discrimination in the same areas of education, employment, etc.
              A regulation of the same year, Maternity Leave and Pay Regulations allowed women statutory leave with pay
              up to a year. The EOC, Equal Opportunities Commission is a strong factor involved with promoting women’s
              rights as well. We see that past solutions that have worked before may be viable options for the empowerment
              of women in developing nations as well.</font>
            <br><br>
              <font style="color:blue">
              The United Kingdom, on a national level, is an advocator of the empowerment of women’s rights. The United
              Kingdom National Committee for UN Women is a postulation for UN Women in the United Kingdom. The
              UKNC for Women focuses its work on influencing and raising awareness on a national and international level
              to promote change for the treatment of women. As a self-funding organization, approximately 90% of income
              is returned directly toward the UN Women programs. The United Kingdom also has many more informal
              associations aiming for the empowerment of women, including “Women Empowered”.</font>
            <br><br>
              <font style="color:#05AFEF">
              The United Kingdom acknowledges the differentiating situations within all nations, primarily developing
              nations. We see that support from the government, and various other organizations including NGO’s can be
              a viable option for women empowerment, as proven within our nation. Within developing nations, especially
              those with a developing governmental system, third party support from nations such as ourselves is fundamental
              towards the empowerment of women. As a sovereign member state of the United Nations, The United Kingdom
              of Great Britain and Northern Ireland is greatly looking forward towards discussing possible solutions and plans
              of action.</font>
